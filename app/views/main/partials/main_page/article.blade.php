                        <div class="art-article"><p>Zarząd koła podjął decyzję o powrocie do wsp&oacute;łorganizowania
                                z Zarządem Okręgowym w Piotrkowie Trybunalskim dorocznego Hubertusa
                                Spalskiego. W tym roku polowania odbędą się 8 października. Podobnie, jak w
                                poprzednich latach, koło otrzyma 5 imiennych zaproszeń na hubertowiny z
                                możliwością polowania w kołach wsp&oacute;łorganizujących obchody. Chętnych
                                do uczestnictwa prosimy o zgłaszanie się do końca sierpnia do łowczego.</p>
                            <p><br/> Nasze koło przeprowadzi polowanie typu szwedzkiego i w związku z tym
                                łowczy prosi o zgłoszenie się do niego sześciu myśliwych, kt&oacute;rzy
                                zadeklarują udostępnienie swoich samochod&oacute;w terenowych do rozwożenia
                                myśliwych na zwyżki. Tych sześciu koleg&oacute;w także będzie mogło wziąć
                                udział w polowaniu, niezależnie od przyznanych centralnie zaproszeń. Jest
                                więc okazja do szerokiej reprezentacji naszego koła na najważniejszym
                                święcie polskich myśliwych. Zgłoszenia do końca sierpnia.</p>
                            <p><br/> Po polowaniu odbędzie się biesiada myśliwska ( 135 zł/os) w hotelu
                                Prezydent. Łowczy zaprasza do uczestnictwa w biesiadzie większą grupą. W
                                celu rezerwacji całego stołu prosi on o deklaracje uczestnictwa w imprezie
                                najp&oacute;źniej do południa 19 sierpnia (piątek). Mile widziane osoby
                                towarzyszące.</p>
